"""
Affine transformation module for S-box generation over GF(2^8).
Implements bijective affine transformations for generating custom S-boxes.
"""

import numpy as np
from typing import Tuple, Union


def gf2_multiply(a: int, b: int) -> int:
    """
    Multiply two elements in GF(2^8) using the irreducible polynomial x^8 + x^4 + x^3 + x + 1.
    
    Args:
        a: First operand (0-255)
        b: Second operand (0-255)
    
    Returns:
        Product in GF(2^8)
    """
    result = 0
    while b:
        if b & 1:
            result ^= a
        a <<= 1
        if a & 0x100:
            a ^= 0x11b  # Irreducible polynomial
        b >>= 1
    return result & 0xff


def gf2_inverse(a: int) -> int:
    """
    Compute multiplicative inverse in GF(2^8) using Extended Euclidean Algorithm.
    
    Args:
        a: Element in GF(2^8) (1-255)
    
    Returns:
        Multiplicative inverse of a
    """
    if a == 0:
        return 0
    
    # Extended GCD for polynomials
    t = 0
    new_t = 1
    r = 0x11b  # Irreducible polynomial
    new_r = a
    
    while new_r != 0:
        quotient = r // new_r if r >= new_r else 0
        t, new_t = new_t, t ^ (quotient * new_t)
        r, new_r = new_r, r ^ (quotient * new_r)
    
    if r > 1:
        return 0  # Not invertible
    return t & 0xff


def matrix_vector_multiply_gf2(matrix: np.ndarray, vector: np.ndarray) -> np.ndarray:
    """
    Multiply a matrix by a vector in GF(2).
    
    Args:
        matrix: 8x8 matrix over GF(2)
        vector: 8-element binary vector
    
    Returns:
        Result vector in GF(2)
    """
    result = np.zeros(8, dtype=int)
    for i in range(8):
        result[i] = int(np.dot(matrix[i], vector) % 2)
    return result


def affine_transform(value: int, matrix: np.ndarray, constant: np.ndarray) -> int:
    """
    Apply affine transformation y = Ax + b in GF(2).
    
    Args:
        value: Input value (0-255)
        matrix: 8x8 invertible matrix over GF(2)
        constant: 8-element constant vector over GF(2)
    
    Returns:
        Transformed value (0-255)
    """
    # Convert value to binary vector
    vector = np.array([(value >> i) & 1 for i in range(8)], dtype=int)
    
    # Apply matrix transformation
    result = matrix_vector_multiply_gf2(matrix, vector)
    
    # Add constant vector
    result = (result + constant) % 2
    
    # Convert back to integer
    output = 0
    for i in range(8):
        output |= (result[i] << i)
    
    return output


def inverse_affine_transform(value: int, matrix: np.ndarray, constant: np.ndarray) -> int:
    """
    Apply inverse affine transformation x = A^(-1)(y - b) in GF(2).
    
    Args:
        value: Input value (0-255)
        matrix: 8x8 invertible matrix over GF(2)
        constant: 8-element constant vector over GF(2)
    
    Returns:
        Inverse transformed value (0-255)
    """
    # Convert value to binary vector
    vector = np.array([(value >> i) & 1 for i in range(8)], dtype=int)
    
    # Subtract constant vector
    vector = (vector - constant) % 2
    
    # Compute matrix inverse
    matrix_inv = compute_matrix_inverse_gf2(matrix)
    
    # Apply inverse matrix transformation
    result = matrix_vector_multiply_gf2(matrix_inv, vector)
    
    # Convert back to integer
    output = 0
    for i in range(8):
        output |= (result[i] << i)
    
    return output


def compute_matrix_inverse_gf2(matrix: np.ndarray) -> np.ndarray:
    """
    Compute the inverse of an 8x8 matrix over GF(2) using Gaussian elimination.
    
    Args:
        matrix: 8x8 invertible matrix over GF(2)
    
    Returns:
        Inverse matrix over GF(2)
    """
    # Create augmented matrix [A|I]
    aug = np.hstack([matrix.copy(), np.eye(8, dtype=int)])
    
    # Forward elimination
    for col in range(8):
        # Find pivot
        pivot_row = None
        for row in range(col, 8):
            if aug[row, col] == 1:
                pivot_row = row
                break
        
        if pivot_row is None:
            continue
        
        # Swap rows
        aug[[col, pivot_row]] = aug[[pivot_row, col]]
        
        # Eliminate column
        for row in range(8):
            if row != col and aug[row, col] == 1:
                aug[row] = (aug[row] + aug[col]) % 2
    
    # Extract inverse matrix
    return aug[:, 8:]


def is_bijective(sbox: np.ndarray) -> bool:
    """
    Check if S-box is bijective (permutation).
    
    Args:
        sbox: S-box array of size 256
    
    Returns:
        True if S-box is bijective
    """
    return len(np.unique(sbox)) == 256 and len(sbox) == 256


def generate_affine_sbox(matrix: np.ndarray, constant: np.ndarray) -> np.ndarray:
    """
    Generate a complete 256-entry S-box using affine transformation.
    
    Args:
        matrix: 8x8 invertible matrix over GF(2)
        constant: 8-element constant vector over GF(2)
    
    Returns:
        256-entry S-box array
    """
    sbox = np.zeros(256, dtype=np.uint8)
    for i in range(256):
        sbox[i] = affine_transform(i, matrix, constant)
    return sbox


def verify_matrix_invertibility_gf2(matrix: np.ndarray) -> bool:
    """
    Verify if matrix is invertible over GF(2) by checking rank.
    
    Args:
        matrix: 8x8 matrix over GF(2)
    
    Returns:
        True if matrix is invertible (rank == 8)
    """
    m = matrix.copy().astype(float)
    
    # Gaussian elimination to compute rank
    rank = 0
    for col in range(8):
        # Find pivot
        pivot_found = False
        for row in range(rank, 8):
            if m[row, col] % 2 == 1:
                # Swap
                m[[rank, row]] = m[[row, rank]]
                pivot_found = True
                break
        
        if not pivot_found:
            continue
        
        # Eliminate column
        for row in range(8):
            if row != rank and m[row, col] % 2 == 1:
                m[row] = (m[row] + m[rank]) % 2
        
        rank += 1
    
    return rank == 8


